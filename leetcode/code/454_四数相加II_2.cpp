using namespace std;

class Solution {
public:
    int fourSumCount(vector<int>& nums1, vector<int>& nums2, vector<int>& nums3, vector<int>& nums4) {
        int result = 0;
        unordered_map<int,int> half; // 存储nums1+nums2的和

        for(auto num1 : nums1){
            for(auto num2 : nums2)
                half[num1+num2]++;
        }

        for(auto num3 : nums3){
            for(auto num4 : nums4)
                if(half.count(-num3-num4))
                    result += half[-num3-num4];
        }

        return result;
    }
};

